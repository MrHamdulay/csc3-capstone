import math
a=math.sqrt(2)
b=math.sqrt(2 + a)
c=math.sqrt(2 + b)
d=math.sqrt(2 + c)
e=math.sqrt(2 + d)
f=math.sqrt(2 + e)
g=math.sqrt(2 + f)
h=math.sqrt(2 + g)
pi=2*(2/a)*(2/b)*(2/c)*(2/d)*(2/e)*(2/f)*(2/g)*(2/h)
pie=round(pi,3)
print("\nApproximation of pi:", pie)
z=eval(input("Enter the radius:\n"))
y=pi*(z*z)
x=round(y,3)
print("Area:", x)
